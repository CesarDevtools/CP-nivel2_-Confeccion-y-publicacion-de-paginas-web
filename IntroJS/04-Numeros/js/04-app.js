// incremento o decremento de uno en uno 

let puntos = 5;


puntos++; // Incrementa en 1
puntos--; // Decrementa en 1


++puntos; // Incrementa en 1 (prefijo)
--puntos; // Decrementa en 1 (prefijo)

puntos += 5; // Incrementa en 5
puntos -= 3; // Decrementa en 3



console.log(puntos); // Muestra el valor final de puntos